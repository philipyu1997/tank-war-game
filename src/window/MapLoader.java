    public MapLoader(Handler handler) {

        this.handler = handler;
        background = tex.background;
        obj_wall1 = tex.spr_wall[0];
        obj_wall2 = tex.spr_wall[1];

    }

    public void loadBackground(Graphics g) {

        int bgWidth = background.getWidth();
        int bgHeight = background.getHeight();

        int fillBgX = GAME_WIDTH / bgWidth;
        int fillBgY = GAME_HEIGHT / bgHeight;

        // LOADS BACKGROUND
        for (int col = 0; col <= fillBgY; ++col) {
            for (int row = 0; row <= fillBgX; ++row) {
                g.drawImage(background, row * bgWidth, col * bgHeight, bgWidth, bgHeight, null);
            }
        }

    }

